export function getTranslation(lang: string, key: string) {
  const keys = key.split('.');
  let value: any = translations[lang as keyof typeof translations];
  for (const k of keys) {
    value = value?.[k];
  }
  return value || key;
}

export function useTranslation(lang: string) {
  return (key: string) => getTranslation(lang, key);
}
